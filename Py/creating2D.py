
# Membuat daftar 2D
matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

# Mengakses elemen dalam daftar 2D
print(matrix[0][0]) # Keluaran: 1
print(matrix[1][2]) # Keluaran: 6
print(matrix[2][1]) # Keluaran: 8
#Pada contoh di atas, matriks adalah sebuah daftar 2D dengan 3 baris dan 3 kolom. Setiap elemen dalam daftar matriks adalah daftar bagian dalam yang mewakili sebuah baris. Anda dapat mengakses setiap elemen dalam daftar 2D menggunakan pengindeksan, di mana indeks pertama menentukan baris dan indeks kedua menentukan kolom.

