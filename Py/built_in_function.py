# TIPE DATA INTEGER : 
int() # digunakan untuk mengubah nilai yang di tentukan menjadi bilangan bulat . Contoh nya : 

x = int("12")
y = int(12.5)


print(x)
print(y)

# output :  12  12




# TIPE DATA FLOAT : 
pow() # -> digunakan untuk mendapatkan pangkat dari suatu bilangan . Contohnya : 
x = pow(3, 3) # 3 pangkat 3 
y = powe(3, -3)  # 3 pangkat -3 

print(x)
print(y)

# output :  27 0.015625




# Tipe Data String 
str() # -> digunakan untuk mengubah objek menjadi string . Contoh nya : 
x = str(12)
y   = str(12.5)


print(type(x))
print(type(y))


# Output : 
# <class 'str'>
# <class 'str'>